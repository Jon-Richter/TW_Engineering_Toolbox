# Table 1A DRY minimum dressed dimensions logic
_NOM_RE = re.compile(r"^\s*(\d+(?:\.\d+)?|\d+\s*/\s*\d+)\s*[xX]\s*(\d+(?:\.\d+)?|\d+\s*/\s*\d+)\s*$")

def _frac_or_float(s: str) -> float:
    s = s.strip().replace(" ", "")
    if "/" in s:
        a, b = s.split("/", 1)
        return float(a) / float(b)
    return float(s)

def parse_nominal_size(nominal: str) -> Tuple[float, float]:
    m = _NOM_RE.match(nominal or "")
    if not m:
        raise ValueError(f"Invalid nominal size {nominal!r}. Expected like '2x10'.")
    return _frac_or_float(m.group(1)), _frac_or_float(m.group(2))

def nominal_to_actual_dry_min(nominal: str) -> Tuple[float, float]:
    """Implements Table 1A (DRY minimum dressed dimensions) category logic."""
    t, w = parse_nominal_size(nominal)

    if abs(t - 0.75) < 1e-9: t_act = 0.625
    elif abs(t - 1.0) < 1e-9: t_act = 0.75
    elif abs(t - 1.25) < 1e-9: t_act = 1.0
    elif abs(t - 1.5) < 1e-9: t_act = 1.25
    elif abs(t - 2.0) < 1e-9: t_act = 1.5
    elif abs(t - 2.5) < 1e-9: t_act = 2.0
    elif abs(t - 3.0) < 1e-9: t_act = 2.5
    elif abs(t - 3.5) < 1e-9: t_act = 3.0
    elif abs(t - 4.0) < 1e-9: t_act = 3.5
    elif abs(t - 4.5) < 1e-9: t_act = 4.0
    elif 5.0 <= t <= 6.0: t_act = t - 0.5
    elif 7.0 <= t <= 15.0: t_act = t - 0.75
    elif t >= 16.0: t_act = t - 1.0
    else: t_act = t

    if w <= 7.0:
        w_act = w - 0.5
    elif w < 16.0:
        w_act = w - 0.75
    else:
        w_act = w - 1.0

    if t_act <= 0 or w_act <= 0:
        raise ValueError(f"Computed nonpositive dressed size for {nominal!r} -> ({t_act}, {w_act}).")

    return round(t_act, 3), round(w_act, 3)

def _canon(s: str) -> str:
    return re.sub(r"\s+"," ",str(s).strip()).upper()

def infer_size_classification_from_nominal(table: str, species: str, grade: str, nominal_size: str) -> str:
    t = str(table).strip()
    sp = str(species).strip()
    gr = str(grade).strip()
    avail = sorted({k[3] for k in ALLOWABLES.keys() if k[0] == t and k[1] == sp and k[2] == gr})
    if not avail:
        raise KeyError(f"No allowables for (table={table}, species={species}, grade={grade}).")
    if len(avail) == 1:
        return avail[0]

    t_nom, w_nom = parse_nominal_size(nominal_size)
    avail_map = {_canon(a): a for a in avail}
    candidates = []

    if t_nom in (3.0, 4.0) and _canon('2-1/2"-4" thick') in avail_map:
        candidates.append(avail_map[_canon('2-1/2"-4" thick')])
    if t_nom >= 3.0 and _canon('2-1/2" & wider') in avail_map:
        candidates.append(avail_map[_canon('2-1/2" & wider')])

    if w_nom == 4.0 and _canon('4" wide') in avail_map:
        candidates.append(avail_map[_canon('4" wide')])
    if w_nom in (2.0,3.0,4.0) and _canon('2" - 4" wide') in avail_map:
        candidates.append(avail_map[_canon('2" - 4" wide')])
    if w_nom in (5.0,6.0) and _canon('5" - 6" wide') in avail_map:
        candidates.append(avail_map[_canon('5" - 6" wide')])
    if w_nom == 8.0 and _canon('8" wide') in avail_map:
        candidates.append(avail_map[_canon('8" wide')])
    if w_nom == 10.0 and _canon('10" wide') in avail_map:
        candidates.append(avail_map[_canon('10" wide')])
    if w_nom == 12.0 and _canon('12" wide') in avail_map:
        candidates.append(avail_map[_canon('12" wide')])

    if _canon('2" & wider') in avail_map:
        candidates.append(avail_map[_canon('2" & wider')])

    for c in candidates:
        if c in avail:
            return c

    raise ValueError(f"Cannot infer size_classification for available={avail} with nominal_size={nominal_size!r}")

@dataclass(frozen=True)
class WoodRecord:
    table: str
    species: str
    grade: str
    nominal_size: str
    size_classification: str
    actual_thickness_in: float
    actual_width_in: float
    reference: Dict[str, object]
    factors: Dict[str, float]

def get_record(table: str, species: str, grade: str, size_classification: str, nominal_size: str) -> WoodRecord:
    key = (str(table).strip(), str(species).strip(), str(grade).strip(), str(size_classification).strip())
    if key not in ALLOWABLES:
        raise KeyError(f"No allowables for {key}")
    ref = ALLOWABLES[key]
    fac = ADJ_FACTORS.get(key, {"Cf": 1.0, "Cm": 1.0, "Cr": 1.0, "Cfu": 1.0})
    t_act, w_act = nominal_to_actual_dry_min(nominal_size)
    return WoodRecord(
        table=key[0], species=key[1], grade=key[2],
        nominal_size=nominal_size, size_classification=key[3],
        actual_thickness_in=t_act, actual_width_in=w_act,
        reference=ref, factors=fac
    )

def get_record_auto_size_class(table: str, species: str, grade: str, nominal_size: str) -> WoodRecord:
    sc = infer_size_classification_from_nominal(table, species, grade, nominal_size)
    return get_record(table, species, grade, sc, nominal_size)
